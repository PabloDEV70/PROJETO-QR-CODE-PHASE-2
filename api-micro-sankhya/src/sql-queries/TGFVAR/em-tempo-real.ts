export const emTempoRealResumo = `
SELECT
    COUNT(*) AS total,
    SUM(CASE WHEN CAB.STATUSNOTA = 'A' THEN 1 ELSE 0 END) AS atendimento,
    SUM(CASE WHEN CAB.STATUSNOTA = 'L' THEN 1 ELSE 0 END) AS liberada,
    SUM(CASE WHEN CAB.STATUSNOTA = 'P' THEN 1 ELSE 0 END) AS pendente,
    SUM(CASE WHEN TOPV.ATUALEST = 'B' THEN 1 ELSE 0 END) AS baixa_estoque,
    SUM(CASE WHEN TOPV.ATUALEST = 'E' THEN 1 ELSE 0 END) AS entrada_estoque,
    SUM(CASE WHEN TOPV.ATUALEST = 'N' THEN 1 ELSE 0 END) AS sem_movimentacao,
    SUM(CASE WHEN TOPV.ATUALEST = 'R' THEN 1 ELSE 0 END) AS reserva_estoque,
    ISNULL(SUM(CAB.VLRNOTA), 0) AS valor_total

FROM TGFVAR VAR

INNER JOIN TGFCAB CAB
    ON CAB.NUNOTA = VAR.NUNOTA

LEFT JOIN TGFTOP TOPV
    ON TOPV.CODTIPOPER = CAB.CODTIPOPER
   AND TOPV.DHALTER = (
        SELECT MAX(TOP2.DHALTER)
        FROM TGFTOP TOP2
        WHERE TOP2.CODTIPOPER = CAB.CODTIPOPER
          AND TOP2.DHALTER <= CAB.DTNEG
   )

WHERE VAR.NUNOTA IN (
    SELECT TOP(200) V2.NUNOTA
    FROM TGFVAR V2
    ORDER BY V2.NUNOTA DESC
)
`;
