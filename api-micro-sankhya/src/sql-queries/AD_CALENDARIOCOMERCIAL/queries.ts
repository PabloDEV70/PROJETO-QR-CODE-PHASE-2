export const calendarioStats = `
SELECT
    COUNT(*) AS total,
    SUM(CASE WHEN cal.STATUS = 'A' THEN 1 ELSE 0 END) AS ativos,
    SUM(CASE WHEN cal.STATUS = 'F' THEN 1 ELSE 0 END) AS finalizados,
    SUM(CASE WHEN cal.STATUS = 'C' THEN 1 ELSE 0 END) AS cancelados,
    COUNT(DISTINCT cal.CODVEICULO) AS veiculos_distintos,
    COUNT(DISTINCT cal.CODPARC) AS parceiros_distintos
FROM SANKHYA.AD_CALENDARIOCOMERCIAL cal
`;

