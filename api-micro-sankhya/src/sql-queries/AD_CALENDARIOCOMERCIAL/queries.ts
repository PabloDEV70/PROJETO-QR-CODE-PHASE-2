export const calendarioList = `
SELECT
    cal.NUCALENDARIO,
    cal.CODVEICULO,
    v.PLACA,
    CAST(v.MARCAMODELO AS VARCHAR(200)) AS MARCAMODELO,
    cal.CODPARC,
    CAST(ISNULL(p.RAZAOSOCIAL, '') AS VARCHAR(200)) AS PARCEIRO,
    cal.DHINIC,
    cal.DHFIN,
    cal.STATUS,
    cal.TIPO,
    CAST(ISNULL(cal.OBSERVACAO, '') AS VARCHAR(MAX)) AS OBSERVACAO,
    cal.CODEMP,
    cal.CODUSU,
    CAST(ISNULL(u.NOMEUSU, '') AS VARCHAR(200)) AS USUARIO
FROM SANKHYA.AD_CALENDARIOCOMERCIAL cal
LEFT JOIN SANKHYA.TGFVEI v ON v.CODVEICULO = cal.CODVEICULO
LEFT JOIN SANKHYA.TGFPAR p ON p.CODPARC = cal.CODPARC
LEFT JOIN SANKHYA.TSIUSU u ON u.CODUSU = cal.CODUSU
ORDER BY cal.DHINIC DESC
OFFSET @offset ROWS FETCH NEXT @limit ROWS ONLY
`;

