export const requisicoesPendentesManutencao = `
SELECT TOP 500
  CAB.NUNOTA,
  ITE.CODPROD,
  PRO.DESCRPROD,
  ITE.CONTROLE,
  ITE.QTDNEG,
  ITE.QTDENTREGUE,
  (ITE.QTDNEG - ITE.QTDENTREGUE) AS QTDPENDENTE,
  CAST(CAB.OBSERVACAO AS VARCHAR(500)) AS OBSERVACAO,
  CAB.AD_PRIORIDADE,
  CONVERT(VARCHAR(10), CAB.DTNEG, 103) AS DTNEG,
  CONVERT(VARCHAR(10), CAB.AD_DTLIMITE, 103) AS DTLIMITE,
  CONVERT(VARCHAR(19), LIB.DHLIB, 120) AS DHLIB,
  CAB.CODVEICULO,
  CASE CAB.CODVEICULO WHEN 0 THEN 'Uso e Consumo' ELSE VEI.PLACA END AS APLICACAO,
  VEI.AD_TAG AS VEICULOTAG,
  CAST(VEI.MARCAMODELO AS VARCHAR(200)) AS MARCAMODELO,
  USU.NOMEUSU,
  CASE CAB.AD_PRIORIDADE
    WHEN 0 THEN 1 WHEN 1 THEN 3 WHEN 2 THEN 5 WHEN 3 THEN 10 WHEN 4 THEN 30 ELSE 0
  END AS DIAS_LIMITE,
  DATEADD(DAY,
    CASE CAB.AD_PRIORIDADE WHEN 0 THEN 1 WHEN 1 THEN 3 WHEN 2 THEN 5 WHEN 3 THEN 10 WHEN 4 THEN 30 ELSE 0 END,
    LIB.DHLIB
  ) AS DH_LIMITE_CALC,
  DATEDIFF(DAY, GETDATE(), DATEADD(DAY,
    CASE CAB.AD_PRIORIDADE WHEN 0 THEN 1 WHEN 1 THEN 3 WHEN 2 THEN 5 WHEN 3 THEN 10 WHEN 4 THEN 30 ELSE 0 END,
    LIB.DHLIB
  )) AS CONTROLE_DIAS,
  CASE
    WHEN CAB.AD_PRIORIDADE = 4 AND DATEDIFF(DAY, GETDATE(), CAB.AD_DTLIMITE) <= 1 THEN 'PROGRAMADA URGENTE'
    ELSE CASE CAB.AD_PRIORIDADE
      WHEN 0 THEN 'EMERGENCIA'
      WHEN 1 THEN 'MUITO URGENTE'
      WHEN 2 THEN 'URGENTE'
      WHEN 3 THEN 'POUCO URGENTE'
      WHEN 4 THEN 'PROGRAMADA'
    END
  END AS PRIORIDADE,
  CASE WHEN CAB.AD_PRIORIDADE IS NOT NULL THEN CONCAT(
    CASE CAB.AD_PRIORIDADE WHEN 0 THEN 0 WHEN 1 THEN 1 WHEN 2 THEN 2 WHEN 3 THEN 5 WHEN 4 THEN 7 END,
    ' Dia(s)', CASE CAB.AD_PRIORIDADE WHEN 4 THEN ' ou +' ELSE '' END
  ) ELSE NULL END AS PRAZO
FROM TGFCAB CAB WITH (NOLOCK)
JOIN TGFITE ITE ON ITE.NUNOTA = CAB.NUNOTA
JOIN TGFPRO PRO ON PRO.CODPROD = ITE.CODPROD
LEFT JOIN TGFVEI VEI ON VEI.CODVEICULO = CAB.CODVEICULO
JOIN TSIUSU USU ON USU.CODUSU = COALESCE(CAB.CODUSUINC, CAB.CODUSU)
LEFT JOIN TSILIB LIB ON LIB.NUCHAVE = CAB.NUNOTA
WHERE (ITE.QTDNEG - ITE.QTDENTREGUE) > 0
  AND CAB.TIPMOV = 'Q'
  AND CAB.STATUSNOTA = 'L'
  AND CAB.CODTIPOPER = 57
  AND CAB.NUMCOTACAO IS NULL
  AND ITE.NUREM IS NULL
  AND ITE.CODPROD NOT IN (5568, 6689, 8076)
ORDER BY CONTROLE_DIAS, CAB.AD_PRIORIDADE, CAB.NUNOTA
`;
